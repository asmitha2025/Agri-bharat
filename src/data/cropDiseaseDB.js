// Return a random disease from the selected crop, or a general disease if crop not in DB
export function getDemoDisease(selectedCrop) {
  let key = selectedCrop;

  // If "Auto-Detect" or unknown, pick a random crop from DB
  if (!CROP_DISEASE_DB[key]) {
    const keys = Object.keys(CROP_DISEASE_DB);
    key = keys[Math.floor(Math.random() * keys.length)];
  }

  const diseases = CROP_DISEASE_DB[key];
  // Randomly pick 1–2 diseases from the crop's list
  const shuffled = [...diseases].sort(() => Math.random() - 0.5);
  const count = Math.min(shuffled.length, Math.random() > 0.5 ? 2 : 1);
  return {
    detected: true,
    cropDetected: key.split('/')[0].trim(),
    overallRisk: shuffled[0].severity,
    diagnoses: shuffled.slice(0, count),
    soilRecommendation: 'Conduct soil test and apply recommended lime/gypsum based on results to improve crop resistance.',
    nearbySpreadRisk: shuffled[0].severity === 'High'
      ? 'High — disease can spread to neighboring fields. Create isolation zone and avoid moving equipment between fields without disinfection.'
      : 'Low to medium — monitor neighboring fields weekly for early symptoms.',
  };
}
